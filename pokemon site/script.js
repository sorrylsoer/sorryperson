  function generateRandomBuild() {
    alert("Good Luck!");
  
    const buildDisplay = document.getElementById('buildDisplay');
    buildDisplay.innerHTML = ''; // Clear previous content
  
    const randomChampion = championsList[Math.floor(Math.random() * championsList.length)];
    const championElement = document.createElement('div');
    championElement.classList.add('champion');
    championElement.innerHTML = `
      <h2>Champion:</h2>
      <p>${randomChampion.name}</p>
      <img src="${randomChampion.image}" alt="${randomChampion.name} Image" />
    `;
    buildDisplay.appendChild(championElement);
  
    for (let i = 0; i < 3; i++) {
      const randomItem = itemsList[Math.floor(Math.random() * itemsList.length)];
  
      const itemElement = document.createElement('div');
      itemElement.classList.add('item');
  
      if (randomItem.image) {
        const itemContent = `
          <h2>Item ${i + 1}:</h2>
          <p>${randomItem.name}</p>
          <img src="${randomItem.image}" alt="${randomItem.name} Image" />
        `;
        itemElement.innerHTML = itemContent;
      } else {
        const itemContent = `
          <h2>Item ${i + 1}:</h2>
          <p>${randomItem.name}</p>
          <p>No image available for ${randomItem.name}</p>
        `;
        itemElement.innerHTML = itemContent;
      }
  
      buildDisplay.appendChild(itemElement);
    }
    
  }